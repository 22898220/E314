void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim) {
	/* Prevent unused argument(s) compilation warning */
	UNUSED(htim);

	if (htim == &htim2)
	{
		if (midButtonState== 0)
		{
			HAL_TIM_Base_Stop_IT(&htim2);
			GPIOA->ODR = GPIOA->ODR ^ (0b1<<8); // if low make high, if high make low(GPIOA->IDR & 0b1)
			toggle_menu = 1;
			level = 0;
			node = 0;
			midButtonState=1;
		}

		if(downButtonState == 0){
			HAL_TIM_Base_Stop_IT(&htim2);
			downButtonState = 1;
			if(level == 0){
				level = 1;
				node = 0;
			}
			else if(((level == 1)&&(node ==0))){
				level = 2;
				node = 0;
				branch = 0;
			}
			else if(((level == 1)&&(node ==1))){
				level = 2;
				node = 4;
				branch = 1;
			}
			else if((level == 2)&&(branch == 1)){
				level = 3;
				node = 0;
			}
			traverse = 1;
		}

		if(upButtonState == 0){
			HAL_TIM_Base_Stop_IT(&htim2);
			upButtonState = 1;
			if(((level == 2)&&(node < 4))){
				level = 1;
				node = 0;
			}
			if(((level == 2)&&(node >= 4))){
				level = 1;
				node = 1;
			}
			if(level == 3){
				level = 2;
				node = 4;
			}
			traverse = 1;
			}

		if(rightButtonState == 0){
			HAL_TIM_Base_Stop_IT(&htim2);
			rightButtonState = 1;
			if(((level == 1) && (node == 0))){
				node = 1;
			}
			if(((level == 2) && (node < 3) && (branch ==0))){
				node++;
			}
			if(((level == 2) && (4 <= node) && (node< 6) && (branch == 1))){
				node++;
			}
			if(((level == 3) && (node < 8))){
				node++;
			}
			traverse = 1;
		}
		if(leftButtonState == 0){
			HAL_TIM_Base_Stop_IT(&htim2);
			leftButtonState = 1;
			if(level == 1 && node == 1){
				node = 0;
			}
			if((level == 2) && (node> 0) && (branch == 0)){
				node--;
			}
			if((level == 2) && (node > 4) && (branch == 1)){
				node--;
			}
			if((level == 3) && (node>0)){
				node--;
			}
			traverse = 1;
		}
	}

	if (htim == &htim3) {
		sampleADC();
	}
}

